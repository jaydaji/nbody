class Velocity
{
    double _velocityX;
    double _velocityY;

    Velocity(double velocityX, double velocityY)
    {
        this._velocityX = velocityX;
        this._velocityY = velocityY;
    }

    void setX(velocityX)
    {
        this._velocityX = velocityX;
    }

    void setY(velocityY)
    {
        this._velocityY = velocityY;
    }

    double getX()
    {
        return this._velocityX;
    }

    double geY()
    {
        return this._velocityY;
    }
}