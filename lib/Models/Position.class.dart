class Position
{
    double _positionX;
    double _positionY;

    Position(double positionX, double positionY)
    {
        this._positionX = positionX;
        this._positionY = positionY;
    }

    void setX(positionX)
    {
        this._positionX = positionX;
    }

    void setY(positionY)
    {
        this._positionY = positionY;
    }

    double getX()
    {
        return this._positionX;
    }

    double getY()
    {
        return this._positionY;
    }
}